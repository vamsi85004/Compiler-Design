#include <stdio.h>

int main() {
    // Define variables
    int r, a, b, c;

    // Assign values to variables (for example)
    a = 10;
    b = 5;
    c = 2;

    // Compute the expression
    r = (a - b) + (b - c) * 30;

    // Print the result
    printf("r = %d\n", r);

    return 0;
}

